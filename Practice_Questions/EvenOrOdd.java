package Practice_Questions;

public class EvenOrOdd {
    public static void main(String[] args) {
        int number = 29;
        if (number % 2 == 0) {
            System.out.println(number + " is an Even number");
        } else {
            System.out.println(number + " is an Odd number");
        }
    }
}

