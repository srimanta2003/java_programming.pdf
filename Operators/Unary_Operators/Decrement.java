package Unary_Operators;

public class Decrement {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("Value of a: " + a); 
        System.out.println("Value of --a: " + --a); 
        System.out.println("Value of a--: " + a--); 
        System.out.println("Value of a: " + a); 
    }
}
