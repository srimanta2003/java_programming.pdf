   package Unary_Operators;

   public class Increment {
       public static void main(String[] args) {
           int a = 10;
           System.out.println("Value of a: " + a); 
           System.out.println("Value of ++a: " + ++a);
           System.out.println("Value of a++: " + a++);
           System.out.println("Value of a: " + a); 
       }
   }
